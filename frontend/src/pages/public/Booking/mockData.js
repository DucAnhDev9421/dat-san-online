export const generateTimeSlots = () => {
  const slots = []
  for (let hour = 11; hour <= 20; hour++) {
    for (let min = 0; min < 60; min += 30) {
      if (hour === 20 && min > 0) break
      const timeString = `${hour.toString().padStart(2, '0')}:${min.toString().padStart(2, '0')}`
      slots.push(timeString)
    }
  }
  return slots
}

